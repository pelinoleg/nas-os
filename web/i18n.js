(function () {
  let rx = null, map = null;
  function compile() {
    map = window.NAS_I18N.en || {};
    const keys = Object.keys(map).filter(k => k).sort((a, b) => b.length - a.length);
    if (!keys.length) { rx = false; return; }
    const alts = keys.map(k => k.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")).join("|");
    try {
      rx = new RegExp("(?<![А-Яа-яЁё])(?:" + alts + ")(?![А-Яа-яЁё])", "g");
    } catch (e) {           // старый браузер без lookbehind — переводим без границ слов
      rx = new RegExp("(?:" + alts + ")", "g");
    }
  }
  window.nasTr = function (s) {
    if (window.NAS_LANG !== "en" || typeof s !== "string" || !/[А-Яа-яЁё]/.test(s)) return s;
    if (rx === null) compile();
    if (!rx) return s;
    return s.replace(rx, m => map[m] !== undefined ? map[m] : m);
  };
  // язык: localStorage-зеркало (сервер — источник истины, синхронизируется после логина)
  try { window.NAS_LANG = localStorage.getItem("naslang") || "en"; }
  catch (e) { window.NAS_LANG = "en"; }
  // всё, что уходит в innerHTML, проходит через перевод (xterm/CodeMirror innerHTML не используют)
  const d = Object.getOwnPropertyDescriptor(Element.prototype, "innerHTML");
  Object.defineProperty(Element.prototype, "innerHTML", {
    configurable: true,
    get() { return d.get.call(this); },
    set(v) { d.set.call(this, (typeof v === "string") ? window.nasTr(v) : v); }
  });
})();
